                        notEmpty: {
                            message: 'This field can’t be empty'
                        }
                    }
                }
            }
        }).on('success.form.fv', function (e, data) {
            sendMail();
            $('#contact_form').formValidation('resetForm', true);
        });

        function sendMail() {
            console.log('entro');
            var param = new FormData($('#contact_form')[0]);
            $.ajax({
                type: 'POST',
                url: '../email/sendmessage.php',
                data: param,
                dataType: 'json',
                async: true,
                contentType: false,
                processData: false,
                cache: false,
                success: function (data) {
                    var status = data.status;
                    if (status == 'success') {
                        swal("Excelente", "¡Gracias por comunicarte con nosotros!", "success");
                        $('#contact_form')[0].reset();
                    } else {
                        swal("Error", "¡Por favor intente de nuevo!", "error");
                        console.log(status);
                    }
                },
                error: function (error) {
                    console.log(error);
                    swal("Error", "¡Por favor intente de nuevo!", "error");
                }
            });
        }
